// 对二维数组进行排序
// 二维数组首数字升序，首数字相同的，第二位数字降序
// 举例：[[1,2],[1,3],[3,1],[3,2],[4,2]]
// 排序为[[1,3], [1,2], [3,2], [3,1],[4,2]]
// 实现
function sortTwoDimenson(arr) {
    return arr.sort((a, b) => {
        if (a[0] === b[0]) {
            return b[1] - a[1];
        } else {
            return a[0] - b[0]
        }
    })
}